    public void preencherComboFornecedor() {
        FornecedorDAO fDAO = new FornecedorDAO();
        List<Fornecedor> listaFornecedores = fDAO.getFornecedores();

        for (Fornecedor fornecedor : listaFornecedores) {
            cmb_Fornecedor.addItem(fornecedor.getNome());
            FornecedorMap.put(fornecedor.getNome(), fornecedor);
        }

        cmb_Fornecedor.setSelectedIndex(-1);
 
    }

    public void preencherComboCliente() {
        ClienteDAO uDAO = new ClienteDAO();
        List<Cliente> listaClientes = uDAO.getCliente();

        for (Cliente cliente : listaClientes) {
            cmb_Cliente.addItem(cliente.getNome());
            ClienteMap.put(cliente.getNome(), cliente);
        }

        cmb_Cliente.setSelectedIndex(-1);
    }
    
     public void preencherCombProduto() {
        ProdutoDAO pDAO = new ProdutoDAO();
        List<Produto> listaProdutos = pDAO.getProdutos();

        for (Produto produto : listaProdutos) {
            cmb_Produto.addItem(produto.getNome());
            ProdutoMap.put(produto.getNome(), produto);
        }

        cmb_Produto.setSelectedIndex(-1);
    }

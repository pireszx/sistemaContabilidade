    private void btn_VendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_VendaActionPerformed
    Venda v = new Venda();

    // Recupera os nomes selecionados nos JComboBox
    String nomeFornecedorSelecionado = (String) cmb_Fornecedor.getSelectedItem();
    String nomeClienteSelecionado = (String) cmb_Cliente.getSelectedItem();
    String nomeProdutoSelecionado = (String) cmb_Produto.getSelectedItem();
    String pagamentoSelecionado = (String) cmb_Pagamento.getSelectedItem();
    String preco = txt_Preco.getText(); // supondo que você tenha esse JTextField

    // Recupera os objetos correspondentes
    Fornecedor fornecedorSelecionado = FornecedorMap.get(nomeFornecedorSelecionado);
    Cliente clienteSelecionado = ClienteMap.get(nomeClienteSelecionado);
    Produto produtoSelecionado = ProdutoMap.get(nomeProdutoSelecionado);

    // Seta os valores no objeto Venda
    v.setFornecedor(fornecedorSelecionado);
    v.setCliente(clienteSelecionado);
    v.setProduto(produtoSelecionado);
    v.setPagamento(pagamentoSelecionado);
    v.setPreco(preco);

    // Insere a venda no banco
    VendaDAO vDAO = new VendaDAO();
    vDAO.inserir(v);

    // Mensagem de sucesso
    JOptionPane.showMessageDialog(this, "Registro efetuado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

    // Limpa o formulário
    limparFormulario();

    }//GEN-LAST:event_btn_VendaActionPerformed

